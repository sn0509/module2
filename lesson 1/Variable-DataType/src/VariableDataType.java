public class VariableDataType {
    public static void main(String[] args) {
        int a = 10;
        float f=20.5f;
        double d = 20.5;
        String s = "Hà Nội";
        boolean b = true;
        char c='a';

        System.out.println("a = " + a);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("s = " + s);
        System.out.println("b = " + b);
        System.out.println("c = " + a);
    }
}
